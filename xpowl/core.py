def abs(x):
    """
    Возвращает абсолютное значение числа.

    Параметры:
        x: число (int, float) или объект, поддерживающий операцию >= и унарный минус

    Возвращает:
        Абсолютное значение числа |x|

    Исключения:
        TypeError: если аргумент не поддерживает необходимые операции

    Примеры:
        >>> abs(5)
        5
        >>> abs(-3.14)
        3.14
        >>> abs(0)
        0
    """
    try:
        return x if x >= 0 else (-x)
    except Exception as e:
        raise TypeError(f"Ошибка вычисления модуля: {str(e)}")


def is_prime(n):
    """
    Проверяет, является ли число простым.

    Параметры:
        n: целое число для проверки

    Возвращает:
        bool: True если число простое, False в противном случае

    Примеры:
        >>> is_prime(7)
        True
        >>> is_prime(10)
        False
        >>> is_prime(1)
        False
    """
    if not isinstance(n, int):
        raise TypeError(f"Ожидается целое число, получен {type(n).__name__}")
    n = abs(n)
    if n <= 2:
        return True
    for x in range(2, int(n**0.5)+1):
        if n%x == 0:
            return False
    return True

def divisors(n):
    """
    Возвращает все делители числа.

    Параметры:
        n: целое число

    Возвращает:
        list: отсортированный список всех делителей числа n
              Для n = 0 возвращает []
              Для n = 1 возвращает [1]

    Исключения:
        TypeError: если n не целое число
    """
    if not isinstance(n, int):
        raise TypeError(f"Ожидается целое число, получен {type(n).__name__}")
    n = abs(n)
    if is_prime(n):
        return [1, n]
    s = []
    for x in range(1, int(n ** 0.5) + 1):
        if n % x == 0:
            s.append(x)
            s.append(n//x)
    return sorted(s)

